
public class Switchclass {

	public static void main(String[] args) {
	
		
		String classs = "shoaib";  //use the break in switch cases after printout statement. 

		switch(classs)
		{
		
		case "shoaib":
			System.out.println("my name is shoaib");
			break;
		case "rayan":
		   System.out.println("my name is rayan");
		   break;
	
		case "fayzaan":
			System.out.println("my name is fayzaan");
			 break;

		case "amal":
			System.out.println("my name is amal");
			 break;
			
		default:
				System.out.println("no result found");
		}	
			
		}
		
	  
		  }
	
	
	


	//the process of grouping variables and methods into a single unit is called encapsulation.
	//used for data hiding.and security reason.
	//getter and setter methods.
public class Encapsulation {
	
	private int phnnumber;
	private String name;
	private String email;


	public static void main(String[] args) {
	
	
	}


	public int getPhnnumber() {
		return phnnumber;
	}


	public void setPhnnumber(int phnnumber) {
		this.phnnumber = phnnumber;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public String getEmail() {
		return email;
	}


	public void setEmail(String email) {
		this.email = email;
	}

}
